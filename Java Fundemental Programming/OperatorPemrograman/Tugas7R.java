public class Tugas7R {
    public static void main(String[] args) {
        boolean hasilPerbandingan;
        int a = 10;
        int b = 15;

        System.out.println("Nilai a = " + a + "\nNilai b = " + b + "\n");

        hasilPerbandingan = a == b;
        System.out.println("a == b ? " + hasilPerbandingan);

        hasilPerbandingan = a != b;
        System.out.println("a != b ? " + hasilPerbandingan);

        hasilPerbandingan = a > b;
        System.out.println("a > b ? " + hasilPerbandingan);

        hasilPerbandingan = a < b;
        System.out.println("a < b ? " + hasilPerbandingan);

        hasilPerbandingan = a >= b;
        System.out.println("a >= b ? " + hasilPerbandingan);

        hasilPerbandingan = a <= b;
        System.out.println("a <= b ? " + hasilPerbandingan);
    }
}

    
