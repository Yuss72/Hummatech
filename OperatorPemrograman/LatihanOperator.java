public class LatihanOperator {
    public static void main(String[] args) {
        int nilai1 = 10;
        int nilai2 = 5;
        int hasil;

        // Operator Pertambahan
        hasil = nilai1 + nilai2;
        System.out.println("Hasil Penjumlahan: " + hasil);

        // Operator Pengurangan
        hasil = nilai1 - nilai2;
        System.out.println("Hasil Penjumlahan: " + hasil);

        // Operator Perkalian
        hasil = nilai1 * nilai2;
        System.out.println("Hasil Penjumlahan: " + hasil);

        // Operator Permbagian
        hasil = nilai1 / nilai2;
        System.out.println("Hasil Penjumlahan: " + hasil);

        // Operator Modulus
        hasil = nilai1 % nilai2;
        System.out.println("Hasil Penjumlahan: " + hasil);
    }
}